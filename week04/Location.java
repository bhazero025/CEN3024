package week04;

public class Location {

	private int street, ave;
	
	public Location(int street, int ave) {
		this.street = street;
		this.ave = ave;
	}

	public int getStreet() {
		return street;
	}

	public void setStreet(int street) {
		this.street = street;
	}

	public int getAvenue() {
		return ave;
	}

	public void setAve(int ave) {
		this.ave = ave;
	}

}
